#import "UIColor+VexColor.h"

@implementation UIColor (VexColor)

+ (UIColor *)vexBlueColor
{
	return [UIColor colorWithRed:0.16796875f green:0.36328125 blue:0.80078125 alpha:1.0f];
}

+ (UIColor *)vexRedColor
{
	return [UIColor colorWithRed:0.6980392157f green:0.07450980392f blue:0.07058823529f alpha:1.0f];
}

+ (UIColor *)vexYellowColor
{
	return [UIColor colorWithRed:1.0f green:1.0f blue:0.0f alpha:1.0f];
}
@end
